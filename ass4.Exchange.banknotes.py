#แลกเงินและหาจำนวนแบงค์

# 2000 => 1000 => 2 ใบ
# 1500 => พัน 1 ใบ ห้าร้อย 1 ใบ
#

n1= int(input("ป้อนจำนวนเงินของคุณ : "))

if n1>= 1000:
    print("1000 บาท = ", n1//1000,"ใบ")
    n1 = n1 % 1000

if n1>= 500:
    print("500 บาท = ", n1//500,"ใบ")
    n1 = n1 % 500
if n1>= 100:
    print("100 บาท = ", n1//100,"ใบ")
    n1 = n1 % 100